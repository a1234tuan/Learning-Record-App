const dateHash = (date: string): number => {
  let hash = 0;
  for (const char of date) {
    hash = (hash * 31 + char.charCodeAt(0)) >>> 0;
  }
  return hash;
};

export const getDailyMotto = (date: string): string =>
  DAILY_MOTTOS[dateHash(date) % DAILY_MOTTOS.length];
